// ******************* Operators *********************

// 1. Arithmetic Operators : (+,-,*,/,**)
// '+' operator is used to concat two strings also
const yourFirstName = 'Astitva';
const yourLastName = 'Shukla';
console.log(yourFirstName + ' ' + yourLastName); // Astitva Shukla

// 2. Assignment Operator : (=)
let x = 10 + 5;
x += 5; // x = x + 5
x++; // x = x + 1;
x--; // x = x - 1

// 3. Comparison Operators (>,<,<=,>=,==,===) produce boolean values true or false
console.log(2 < 4); // true
console.log(-1 > 0); // false
// == checks for values stored in the variables and === checks for data types also, then only returns true or false

